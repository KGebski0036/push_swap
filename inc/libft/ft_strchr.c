#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	cc;

	cc = (char)c;
	while (*s != cc && *s)
	{
		s++;
	}
	if (*s == cc)
		return ((char *)(s));
	return (0);
}
